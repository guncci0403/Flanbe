package kr.or.ddit.project.model;

// 프로젝트 관련기술
public class PSkillVo {

	private int no;			// 번호(시퀀스)
	private int p_code;		// 프로젝트 코드
	private String ps_no;	// 기술 (자바 : 01, C언어 : 02, 파이썬 : 03, 자바스크립트 : 04, 기타 : 05)
	
	public PSkillVo() {}
	
	public PSkillVo(int no, int p_code, String ps_no) {
		super();
		this.no = no;
		this.p_code = p_code;
		this.ps_no = ps_no;
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public int getP_code() {
		return p_code;
	}
	public void setP_code(int p_code) {
		this.p_code = p_code;
	}
	public String getPs_no() {
		return ps_no;
	}
	public void setPs_no(String ps_no) {
		this.ps_no = ps_no;
	}
	@Override
	public String toString() {
		return "PSkillVo [no=" + no + ", p_code=" + p_code + ", ps_no=" + ps_no + "]";
	}
	
	
}
