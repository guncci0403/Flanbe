package kr.or.ddit.note.model;

// 노트
public class NoteVo {

	private int n_no;			// 글번호
	private String user_id;		// 작성자 아이디
	private int p_code;			// 프로젝트 코드
	private String n_title;		// 업무명
	private String n_cont;		// 업무내용
	private String category;	// 업무 분류 코드
	private String n_import;	// 중요도 (중요 : 01 , 보통 : 02)
	private String n_regdt;		// 작성일시
	private String n_state;		// 업무진행상태 (진행 전 : 01 , 진행 중 : 02 , 완료 : 03)
	private int n_gno;			// 그룹번호
	private int n_parent;		// 부모글 번호
	private int progress;		// 진행률
	private String n_sdt;			// 시작날짜
	private String n_edt;			// 종료날짜
	private String code_name;
	
	
	public NoteVo() {}
	
	
	public NoteVo(int n_no, String user_id, int p_code, String n_title, String n_cont, String category, String n_import,
			String n_regdt, String n_state, int n_gno, int n_parent, int progress, String n_sdt, String n_edt,
			String code_name) {
		super();
		this.n_no = n_no;
		this.user_id = user_id;
		this.p_code = p_code;
		this.n_title = n_title;
		this.n_cont = n_cont;
		this.category = category;
		this.n_import = n_import;
		this.n_regdt = n_regdt;
		this.n_state = n_state;
		this.n_gno = n_gno;
		this.n_parent = n_parent;
		this.progress = progress;
		this.n_sdt = n_sdt;
		this.n_edt = n_edt;
		this.code_name = code_name;
	}


	public int getN_no() {
		return n_no;
	}
	public void setN_no(int n_no) {
		this.n_no = n_no;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public int getP_code() {
		return p_code;
	}
	public void setP_code(int p_code) {
		this.p_code = p_code;
	}
	public String getN_title() {
		return n_title;
	}
	public void setN_title(String n_title) {
		this.n_title = n_title;
	}
	public String getN_cont() {
		return n_cont;
	}
	public void setN_cont(String n_cont) {
		this.n_cont = n_cont;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getN_import() {
		return n_import;
	}
	public void setN_import(String n_import) {
		this.n_import = n_import;
	}
	public String getN_regdt() {
		return n_regdt;
	}
	public void setN_regdt(String n_regdt) {
		this.n_regdt = n_regdt;
	}
	public String getN_state() {
		return n_state;
	}
	public void setN_state(String n_state) {
		this.n_state = n_state;
	}
	public int getN_gno() {
		return n_gno;
	}
	public void setN_gno(int n_gno) {
		this.n_gno = n_gno;
	}
	public int getN_parent() {
		return n_parent;
	}
	public void setN_parent(int n_parent) {
		this.n_parent = n_parent;
	}
	public int getProgress() {
		return progress;
	}
	public void setProgress(int progress) {
		this.progress = progress;
	}
	public String getN_sdt() {
		return n_sdt;
	}
	public void setN_sdt(String n_sdt) {
		this.n_sdt = n_sdt;
	}
	public String getN_edt() {
		return n_edt;
	}
	public void setN_edt(String n_edt) {
		this.n_edt = n_edt;
	}
	
	public String getCode_name() {
		return code_name;
	}

	public void setCode_name(String code_name) {
		this.code_name = code_name;
	}

	@Override
	public String toString() {
		return "NoteVo [n_no=" + n_no + ", user_id=" + user_id + ", p_code=" + p_code + ", n_title=" + n_title
				+ ", n_cont=" + n_cont + ", category=" + category + ", n_import=" + n_import + ", n_regdt=" + n_regdt
				+ ", n_state=" + n_state + ", n_gno=" + n_gno + ", n_parent=" + n_parent + ", progress=" + progress
				+ ", n_sdt=" + n_sdt + ", n_edt=" + n_edt + ", code_name=" + code_name + "]";
	}
	
	
	
}