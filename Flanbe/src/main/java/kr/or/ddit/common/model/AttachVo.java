package kr.or.ddit.common.model;

// 첨부파일
public class AttachVo {
	
	private int a_no;				// 첨부파일 번호(시퀀스)
	private int no;					// 게시글/포트폴리오 번호
	private String a_seq;			// 구분자 (게시글 : 01 , 포트폴리오 : 02)
	private String a_nm;			// 첨부파일 명
	private String a_route;			// 첨부파일 경로
	
	public AttachVo() {}
	
	public AttachVo(int no, int a_no, String a_seq, String a_nm, String a_route) {
		super();
		this.no = no;
		this.a_no = a_no;
		this.a_seq = a_seq;
		this.a_nm = a_nm;
		this.a_route = a_route;
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public int getA_no() {
		return a_no;
	}
	public void setA_no(int a_no) {
		this.a_no = a_no;
	}
	public String getA_seq() {
		return a_seq;
	}
	public void setA_seq(String a_seq) {
		this.a_seq = a_seq;
	}
	public String getA_nm() {
		return a_nm;
	}
	public void setA_nm(String a_nm) {
		this.a_nm = a_nm;
	}
	public String getA_route() {
		return a_route;
	}
	public void setA_route(String a_route) {
		this.a_route = a_route;
	}
	@Override
	public String toString() {
		return "AttachVo [no=" + no + ", a_no=" + a_no + ", a_seq=" + a_seq + ", a_nm=" + a_nm + ", a_route=" + a_route
				+ "]";
	}
	
	
	
}
